    /**
     * Handles Malts in Age Range Button Event 
     */
    private void maltsInAgeRangeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_maltsInAgeRangeBtnActionPerformed
        String ageLowerRangeValue = lowerAgeRangeTxtField.getText();
        String ageUpperRangeValue = upperAgeRangeTxtField.getText();

        //checks whether user has entered data 
        if (ageLowerRangeValue.equals("") && ageUpperRangeValue.equals("")) {
            JOptionPane.showMessageDialog(null, "Error!! Please enter Age Range",
                    "Input Age Range", JOptionPane.ERROR_MESSAGE);
        } //checks whether lower age range value entered by user is numeric or not
        else if (!ageLowerRangeValue.equals("") && !isStringNumeric(ageLowerRangeValue)) {
            JOptionPane.showMessageDialog(null, "Error!!Lower(Left) Age limit should be numeric",
                    "Input Age Range", JOptionPane.ERROR_MESSAGE);
        } //checks whether upper age range value entered by user is numeric or not
        else if (!ageUpperRangeValue.equals("") && !isStringNumeric(ageUpperRangeValue)) {
            JOptionPane.showMessageDialog(null, "Error!!Upper(Right) Age Range must be numeric",
                    "Input Age Range", JOptionPane.ERROR_MESSAGE);
        } else {
            //sets lower limit age value of age to 0
            if (ageLowerRangeValue.equals("")) {
                ageLowerRangeValue = "0";
            }
            //sets upper limit value of age to 100
            if (ageUpperRangeValue.equals("")) {
                ageUpperRangeValue = "100";
            }

            int ageLowerRange = Integer.parseInt(ageLowerRangeValue); //convert String value of lower age to int
            int ageUpperRange = Integer.parseInt(ageUpperRangeValue); //convert String value of upper age to int

            //checks if upper bound of age is less than lower bound
            if (ageUpperRange < ageLowerRange) {
                JOptionPane.showMessageDialog(null, "Error!! Lower(Left) Age Range cannot be greater than Upper(Right) Age Range",
                        "Input Age Range", JOptionPane.ERROR_MESSAGE);
            } else {
                controller.maltsInAgeRange(ageLowerRangeValue, ageUpperRangeValue);
            }
        }
    }//GEN-LAST:event_maltsInAgeRangeBtnActionPerformed

    /**
     * Handles Update Price button event
     */
    private void updatePriceBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatePriceBtnActionPerformed
        String distilleryValue = distilleryTxtField.getText(); //get Distillery from user
        String ageValue = ageTxtField.getText(); //get Age from user
        String priceValue = priceTxtField.getText(); //get Price from user

        //checks whether user has entered  data or not
        if (distilleryValue.equals("") || ageValue.equals("") || priceValue.equals("")) {
            JOptionPane.showMessageDialog(null, "Error!!Distillery, Age and Price value is required inorder to Update price",
                    "Input to Update Price", JOptionPane.ERROR_MESSAGE);
        } //checks whether user has entered numeric value for Age or not
        else if (!isStringNumeric(ageValue)) {
            JOptionPane.showMessageDialog(null, "Error!! Please enter numeric value for Age",
                    "Input to Update Price", JOptionPane.ERROR_MESSAGE);
        } //checks whether user has entered numeric value for Price or not
        else if (!isStringNumeric(priceValue)) {
            JOptionPane.showMessageDialog(null, "Error!!Please enter numeric value for Price",
                    "Input to Update Price", JOptionPane.ERROR_MESSAGE);
        } else {
            controller.updatePrice(distilleryValue, ageValue, priceValue);
        }
    }//GEN-LAST:event_updatePriceBtnActionPerformed

