//method updates the field in the text area and text field
    @Override
    public void update(Observable o, Object arg) {
        if (arg instanceof String) {
            browsing(false);
            messageTxtArea.setText(arg.toString());
            distilleryTxtField.setText("");
            ageTxtField.setText("");
            regionTxtField.setText("");
            priceTxtField.setText("");
            upperAgeRangeTxtField.setText("");
            lowerAgeRangeTxtField.setText("");
        }
        if (arg instanceof ViewState) {
            browsing(true);
            ViewState viewState = (ViewState) arg;
            SingleMalt singleMalt = viewState.getSingleMalt();
            distilleryTxtField.setText(singleMalt.getDistillery());
            ageTxtField.setText(String.valueOf(singleMalt.getAge()));
            regionTxtField.setText(singleMalt.getRegion());
            priceTxtField.setText(String.valueOf(singleMalt.getPrice()));
            messageTxtArea.setText(viewState.getMessageDisplay());

        }
    }

   
    // Method that checks whether the string entered by user is an alphabet or numeric value
    
    private boolean isStringNumeric(String str) {
        for (int i = 0; i < str.length(); i++) {
            if (!Character.isDigit(str.charAt(i))) {
                return false;
            }
        }

        return true;
    }
